public class Cell {
	public final int x;
	public final int y;

	public Cell(int x, int y) {
		this.x = x;
		this.y = y;
	}

	@Override
	public String toString() {
		return x + " " + y;
	}

	/*
	 * Many of Java's conveniences (e.g., Sets, Maps, and their implementing
	 * HashSet and HashMap classes) won't work if you don't implement a
	 * meaningful equals() and hashCode() method. IDEs (like IntelliJ) can do
	 * this for you.
	 */

	@Override
	public boolean equals(Object o) {
		if (this == o)
			return true;
		if (o == null || getClass() != o.getClass())
			return false;

		Cell cell = (Cell) o;

		if (x != cell.x)
			return false;
		if (y != cell.y)
			return false;

		return true;
	}

	@Override
	public int hashCode() {
		int result = x;
		result = 31 * result + y;
		return result;
	}

}
